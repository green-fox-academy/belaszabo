# Write a program that asks for 5 integers in a row,
# then it should print the sum and the average of these numbers like:
#
# Sum: 22, Average: 4.4
a = float(input('a = '))
b = float(input('b = '))
c = float(input('c = '))
d = float(input('d = '))
e = float(input('e = '))
sum = a + b + c + d + e
z = sum / 5
print('Sum:',sum,', Average:',z)